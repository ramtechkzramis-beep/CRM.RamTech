import path from "node:path";
import { randomUUID } from "node:crypto";
import { Font } from "@react-pdf/renderer";

/**
 * Регистрирует шрифт с кириллицей для PDF и возвращает имя семейства,
 * которое нужно подставить в стили документа.
 *
 * У @react-pdf/renderer по умолчанию только Helvetica без кириллицы — без
 * регистрации весь русский текст в документе превратился бы в пустые квадраты.
 *
 * Файлы шрифта лежат в public/, а не рядом в src/pdf/: Vercel гарантированно
 * включает public/ в сборку serverless-функции, а произвольные файлы
 * за пределами бандла — не всегда.
 *
 * Имя семейства — уникальное на каждый вызов (не константа "PTSans").
 * Причина: FontStore из @react-pdf/renderer кэширует разобранные данные
 * шрифта на объекте семейства и переиспользует его между документами
 * в одном процессе — актуально для тёплых serverless-инстансов на Vercel,
 * где несколько запросов подряд попадают в один и тот же процесс. При
 * таком переиспользовании ToUnicode-карта второго и последующих PDF
 * съезжает: текст выглядит верно на глаз, но не копируется и не ищется.
 * Ни Font.reset() (обнуляет данные шрифта, но не кэш промиса загрузки —
 * следующий рендер падает), ни Font.clear() (заодно стирает служебные
 * стандартные шрифты вроде Helvetica, от которых зависит сам рендерер)
 * эту проблему без побочных эффектов не решают. Уникальное имя семейства
 * гарантирует каждому документу собственный, ничем не разделяемый объект
 * шрифта — без обращения к внутренним деталям библиотеки.
 */
export function registerProposalFonts(): string {
  const family = `PTSans-${randomUUID()}`;
  const fontsDir = path.join(process.cwd(), "public", "fonts");

  Font.register({
    family,
    fonts: [
      { src: path.join(fontsDir, "PTSans-Regular.ttf"), fontWeight: "normal" },
      { src: path.join(fontsDir, "PTSans-Bold.ttf"), fontWeight: "bold" },
    ],
  });

  // Перенос по словам react-pdf иногда режет длинные русские слова странно —
  // отключаем дефисацию, точнее без неё, чем с непредсказуемыми переносами.
  Font.registerHyphenationCallback((word) => [word]);

  return family;
}
